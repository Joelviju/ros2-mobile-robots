//ros2 run my_cpp_templates basic_publisher
#include <rclcpp/rclcpp.hpp>
#include <std_msgs/msg/string.hpp>

using namespace std::chrono_literals;

class BasicPublisher : public rclcpp::Node
{
public:
  BasicPublisher()
  : Node("basic_publisher"), count_(0)
  {
    publisher_ = this->create_publisher<std_msgs::msg::String>(
      "chatter", 10);

    timer_ = this->create_wall_timer(
      500ms,
      std::bind(&BasicPublisher::timerCallback, this));
  }

private:
  void timerCallback()
  {
    auto message = std_msgs::msg::String();
    message.data = "Hello ROS 2! Count: " + std::to_string(count_++);

    RCLCPP_INFO(this->get_logger(), "Publishing: '%s'",
                message.data.c_str());

    publisher_->publish(message);
  }

  rclcpp::Publisher<std_msgs::msg::String>::SharedPtr publisher_;
  rclcpp::TimerBase::SharedPtr timer_;
  int count_;
};

int main(int argc, char * argv[])
{
  rclcpp::init(argc, argv);
  rclcpp::spin(std::make_shared<BasicPublisher>());
  rclcpp::shutdown();
  return 0;
}
